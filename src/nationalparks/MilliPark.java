
package nationalparks;


// Kullanılacak olan Milli Park sınıfının yaratılması.
public class MilliPark {
    public String isim;
    public String mekan;
    public int yil;
    public int yuzOlcum;

    public MilliPark(String isim, String mekan, int yil, int yuzOlcum) {
        this.isim = isim;
        this.mekan = mekan;
        this.yil = yil;
        this.yuzOlcum = yuzOlcum;
    }
    
    
}
